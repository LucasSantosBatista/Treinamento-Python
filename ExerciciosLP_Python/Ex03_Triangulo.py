# Receba a base e a altura de um triângulo. Calcule e mostre a sua área

print('Bem vindo ao Calculo de Triangulo')

base = int(input("Digite o valor do base do triangulo (cm): "))
altura = int(input("Digite a altura do triangulo (cm): "))

area = (base * altura) / 2

print('A área do trinagulo é ' +  str(area) + 'cm2')

